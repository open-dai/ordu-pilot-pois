package GeoInfoMngmnt.PropertyMngmnt.V1;

public class PhoneNumberType {

	private String phoneType;
	private String phoneDescription;
	private String phoneCountryCode;
	private String phoneCode;
	private String phoneNumber;
	private String extensionNumber;
	private String fullPhoneString;
	private String priority;

	public String getPhoneType() {
		return phoneType;
	}

	public void setPhoneType(String phoneType) {
		this.phoneType = phoneType;
	}

	public String getPhoneDescription() {
		return phoneDescription;
	}

	public void setPhoneDescription(String phoneDescription) {
		this.phoneDescription = phoneDescription;
	}

	public String getPhoneCountryCode() {
		return phoneCountryCode;
	}

	public void setPhoneCountryCode(String phoneCountryCode) {
		this.phoneCountryCode = phoneCountryCode;
	}

	public String getPhoneCode() {
		return phoneCode;
	}

	public void setPhoneCode(String phoneCode) {
		this.phoneCode = phoneCode;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public String getExtensionNumber() {
		return extensionNumber;
	}

	public void setExtensionNumber(String extensionNumber) {
		this.extensionNumber = extensionNumber;
	}

	public String getFullPhoneString() {
		return fullPhoneString;
	}

	public void setFullPhoneString(String fullPhoneString) {
		this.fullPhoneString = fullPhoneString;
	}

	public String getPriority() {
		return priority;
	}

	public void setPriority(String priority) {
		this.priority = priority;
	}

}
